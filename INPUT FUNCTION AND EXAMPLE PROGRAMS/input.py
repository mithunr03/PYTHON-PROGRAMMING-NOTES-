#input() is a built-in function in Python that allows the user to take input from the console.
# It reads a line from input, converts it into a string (stripping a trailing newline), and returns that.
name =input("Enter your name:")
print(f"hii {name} nice to meet you!")
# name is variable which is used to store the input value from the user.
# and in this line  print(f"hii {name} nice to meet you!") we are using f-string to print the value of the variable name in the output.