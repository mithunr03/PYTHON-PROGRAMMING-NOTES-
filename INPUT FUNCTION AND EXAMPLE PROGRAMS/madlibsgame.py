# we are going to create a madlibs game that will ask the user for input and then print out a story using their input
adjective1 = input("Enter an adjective: ")
adjective2 = input("Enter another adjective: ")
noun1 = input("Enter a noun: ")
noun2 = input("Enter another noun: ")
verb1 = input("Enter a verb: ")
verb2 = input("Enter another verb: ")
print(f"i went to a park and saw a {adjective1} {noun1} {verb1} across the {noun2}. The {noun2} was very {adjective2} and {verb2} all around.")