# so here we are going to see some math built in functions
import math 
math.pi # gives the pi value 

print(math.pi)
math.e # gives the exponential value 
print(math.e)

#the sqrt function gives the square root of a number 
value =  12
result = math.sqrt(value)
print(f"the square toot of {value} is {result}")

# the ceil function is used to round off number , like if you have a value 9.6 it will round off to 10
val = 9.4
result1  = math.ceil(val)
print(result1)

#The floor function is used to round off a number , like if you have a value 9.5 it will round off to 9 
val1 = 11.5
result2 = math.floor(val1)
print(result2)


